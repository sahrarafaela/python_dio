saldo = 450
saque = 200

#sempre retorna um valor booleano: True ou False

print(saldo == saque)  # é igual? False
print(saldo != saque) # é diferente? True
print(saldo < saque) # é menor? False
print(saldo <= saque) # é menor ou igual? False
print(saldo > saque) # é maior? True
print(saldo >= saque) # é maior ou igual? True

