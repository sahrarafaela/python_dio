"""
O QUE SÃO E COMO É UTILIZADO

utiliza-se para realizar operações matemáticas

quais são eles?
* adição: +
* subtração: -
* multiplição: *
* divisão: /
* divisão inteira: //
* módulo: %
* exponenciação: **


A ordem de precedencia é igual na matemática:
* parenteses
* expoentes
* multiplicação e divisão (esquerda para direita)
* soma e subtração


Realizando exemplos com código:
"""


produto_1 = 10
produto_2 = 20
print(produto_1 + produto_2)
print(produto_1 - produto_2)
print(produto_1 * produto_2)
print(produto_1 / produto_2)
print(produto_1 // produto_2)
print(produto_1 ** produto_2)
print(produto_1 % produto_2)

x = 10 + 5 * 4
y = 10 / 2 + 25 * 2 - 2 ** 2

print(x)
print(y)